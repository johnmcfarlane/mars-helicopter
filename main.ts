scene.onHitWall(SpriteKind.Player, function (sprite, location) {
    if (sprite.isHittingTile(CollisionDirection.Bottom)) {
        if (terminalVelocity()) {
            game.over(false)
        }
    } else {
        game.over(false)
    }
})
